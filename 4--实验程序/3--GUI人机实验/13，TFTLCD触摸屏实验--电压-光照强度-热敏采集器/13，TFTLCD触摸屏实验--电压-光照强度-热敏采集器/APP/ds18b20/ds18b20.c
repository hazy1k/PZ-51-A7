{             
	u8 i, j;

	for(j=0; j<8; j++)
	{
		DS18B20_DQ_OUT = 0;	     	  //每写入一位数据之前先把总线拉低1us
		i++;
		DS18B20_DQ_OUT = dat & 0x01;  //然后写入一个数据，从最低位开始
		i=6;
		while(i--); //延时68us，持续时间最少60us
		DS18B20_DQ_OUT = 1;	//然后释放总线，至少1us给总线恢复时间才能接着写入第二个数值
		dat >>= 1;
	}
}

